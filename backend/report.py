# Content Rendering Helpers
# ═══════════════════════════════════════════════════════════════════════════

# Unicode ->  ASCII replacements for PDF core font compatibility
_UNICODE_REPLACEMENTS = {
    "\u2014": "---",  # em dash
    "\u2013": "--",  # en dash
    "\u2018": "'",  # left single quotation
    "\u2019": "'",  # right single quotation
    "\u201c": '"',  # left double quotation
    "\u201d": '"',  # right double quotation
    "\u2026": "...",  # ellipsis
    "\u2022": "*",  # bullet
    "\u2192": "->",  # arrow
    "\u2190": "<-",  # left arrow
    "\u00b0": " deg",  # degree
    "\u00b1": "+/-",  # plus-minus
    "\u00d7": "x",  # multiplication
    "\u00f7": "/",  # division
    "\u2264": "<=",  # less-than-or-equal
    "\u2265": ">=",  # greater-than-or-equal
    "\u2260": "!=",  # not equal
    "\u03b1": "alpha",  # Greek alpha
    "\u03b2": "beta",  # Greek beta
    "\u03b3": "gamma",  # Greek gamma
    "\u03b4": "delta",  # Greek delta
    "\u03b5": "epsilon",
    "\u03bb": "lambda",
    "\u03c0": "pi",
    "\u03c3": "sigma",
    "\u03c9": "omega",
    "\u212b": "Angstrom",
    "\u00c5": "A",  # Angstrom A
}


def _sanitize(text: str) -> str:
    """Replace Unicode characters with ASCII equivalents for PDF core font compatibility."""
    for char, replacement in _UNICODE_REPLACEMENTS.items():
        text = text.replace(char, replacement)
    # Strip remaining non-latin-1 characters
    return text.encode("latin-1", errors="replace").decode("latin-1")


def _render_content(pdf: FPDF, content: str) -> None:
    """
    Render markdown-like content to the PDF.
    Handles code blocks (```...```), inline code (`...`), paragraphs.
    """
    content = _sanitize(content)
    # Split by code blocks
    parts = re.split(r"(```[\s\S]*?```|`[^`]+`)", content)

    for part in parts:
        if not part.strip():
            continue

        if part.startswith("```"):
            # Code block
            _render_code_block(pdf, part)
        elif part.startswith("`") and part.endswith("`"):
            # Inline code
            code_text = part.strip("`")
            pdf.set_font("Courier", "", 7)
            pdf.set_text_color(*C_ACCENT_BLUE)
            pdf.write(4, code_text)
        else:
            # Regular text --  split into paragraphs
            paragraphs = part.split("\n\n")
            for para in paragraphs:
                para = para.strip()
                if not para:
                    continue
                # Handle inline formatting like **bold**, _italic_, etc.
                pdf.set_font("Helvetica", "", 9)
                pdf.set_text_color(*C_BLACK)
                # Simple line-by-line for long text
                pdf.multi_cell(0, 4.5, para, new_x="LMARGIN", new_y="NEXT")
                pdf.set_x(13)


def _render_code_block(pdf: FPDF, block: str) -> None:
    """Render a fenced code block with background and monospace font."""
    # Extract content
    lines = block.strip().split("\n")
    # Remove opening and closing ```
    if lines[0].startswith("```"):
        lines = lines[1:]
    if lines and lines[-1].strip() == "```":
        lines = lines[:-1]
    # Remove optional language tag from first line
    if lines and not lines[0].startswith(" ") and len(lines[0]) < 30:
        lines = (
            lines[1:]
            if not lines[0].startswith(("def ", "import", "from", "class", "```"))
            else lines
        )

    if not lines:
        return

    code_text = "\n".join(lines)
    code_text = _sanitize(code_text)

    # Code block background
    y_before = pdf.get_y()
    x_before = pdf.get_x()

    # Estimate height
    line_count = len(lines)
    line_height = 3.5
    block_height = line_count * line_height + 4  # padding

    # Check if we need a page break
    if y_before + block_height > 270:
        pdf.add_page()
        y_before = pdf.get_y()

    # Draw background
    pdf.set_fill_color(*C_BG_SURFACE)
    pdf.set_draw_color(*C_BORDER)
    pdf.rect(13, y_before, 185, block_height, "DF")

    # Render code
    pdf.set_xy(16, y_before + 2)
    pdf.set_font("Courier", "", 6.5)
    pdf.set_text_color(*C_ACCENT_GREEN)

    for line in lines:
        pdf.set_x(16)
        pdf.cell(0, line_height, line[:120], ln=True)

    pdf.set_y(y_before + block_height + 2)
